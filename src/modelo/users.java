
package modelo;


public class users {
   private String User;
   private String Pass;
   private int Admin;
   private String Nombre;
   private String Ap_p;
   private String Ap_m;
   private String A_nac;
   
    public String getUser() {
        return User;
    }

    public void setUser(String User) {
        this.User = User;
    }

    public String getPass() {
        return Pass;
    }

    public void setPass(String Pass) {
        this.Pass = Pass;
    }

    public int getAdmin() {
        return Admin;
    }

    public void setAdmin(int Admin) {
        this.Admin = Admin;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getAp_p() {
        return Ap_p;
    }

    public void setAp_p(String Ap_p) {
        this.Ap_p = Ap_p;
    }

    public String getAp_m() {
        return Ap_m;
    }

    public void setAp_m(String Ap_m) {
        this.Ap_m = Ap_m;
    }

    public String getA_nac() {
        return A_nac;
    }

    public void setA_nac(String A_nac) {
        this.A_nac = A_nac;
    }
  
   
}
